
 # Entrada de datos
print ("Ingresa un valor en grados centigrados")
centigrados = int(input())


# Operaciones o calculos matematicos
fahrenheit =  Fahrenheit = 32 + (9 * centigrados / 5)
kelvin = (centigrados - 32) * 5 / 9 + 273

# Imprimir o mostrar en pantalla
print(f"En grados Fahrentheit equivale a = {fahrenheit}")
print(f"En grados Kelvin equivale a = {kelvin}")

